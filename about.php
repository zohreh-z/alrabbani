وى پس از پایان تحصیلات ابتدائى در سال 1339 هـ . شمسى وارد حوزه علمیه شهررى (مدرسه برهان) شد و در سال 1340 براى ادامه تحصیل به قم رفت و تا پیروزى انقلاب اسلامى مدارج تحصیلى و تدریس را در حوزه علمیه قم طى کرد. البته در این فاصله به دلایل سیاسى، مدت کوتاهى به مشهد مقدس و نجف اشرف مهاجرت کرد. اشتغالات علمى و حوزوى ایشان تاکنون نیز در کنار انجام مسئولیتهاى مختلف ادامه دارد.
اساتید :
وى سطوح عالیه را نزد اساتید بزرگى همچون حضرات آیات عظام حاج میرزا على مشکینى، حاج شیخ محمد فاضل لنکرانى و حاج سید محمدباقر طباطبائى تلمّذ نمود.
ایشان دروس خارج و فقه اصول را در سال 1347 و در شرایطى آغاز کرد که حوزه علمیه قم از فیض حضور استاد اعظم حضرت امام خمینى (ره) که به نجف تبعید شده بودند، بىبهره بود، بنابراین خارج اصول را از محضر حضرات آیات عظام مرحوم حاج شیخ محمدعلى اراکى و حاج شیخ حسین وحید خراسانى استفاده کرد و خارج فقه را از برکات درس حضرات آیات عظام حاج میرزا على مشکینى، حاج شیخ جواد تبریزى، مرحوم حاج سید محمدرضا گلپایگانى و مرحوم حاج شیخ مرتضى حائرى، بهره برد.
