فعالیتهاى سیاسى ـ مبارزاتى در دوران طاغوت
آیت الله  رى شهرى از ابتداى نهضت اسلامى بویژه تحت تأثیر جریانات 15خرداد 1342 با اهداف و دیدگاههاى حضرت امام(ره) آشنا شد. وى در تاریخ 20/4/1344 توسط ساواک مشهد دستگیر و به زندان محکوم شد.
آیت الله  محمدى رى شهرى در سال 1345 ضمن تمرّد از شرکت در جلسه دادگاه تجدید نظر ساواک به نجف اشرف رفت.
وى در سال 1346 به ایران بازگشت و حدود 18ماه در حوزه علمیه مشهد به تحصیل پرداخت. در همین مدت مجدداً تحت تعقیب ساواک قرار گرفت و در دادگاه تجدید نظر بار دیگر به زندان محکوم شد.
ممنوعیت منبر توسط ساواک
وى در سال 1356 به دلیل سخنرانى علیه رژیم شاه در حسینیه اصفهانیهاى آبادان توسط ساواک ممنوع المنبر شد، در گزارش مورّخ 27/9/1356 ساواک چنین آمده است: «یاد شده بمناسبت ماه محرم سخنانى پیرامون رهبرى در اسلام» ایراد نمود، یاد شده در دنباله سخنرانى شب قبل خود افزود طبق روایات (صحیحه) قبل از ظهور امام زمان باید در این سرزمین حکومت اسلامى برقرار گردد که براى امام زمان سرباز فداکار تربیت نماید».
وى به دلیل سخنرانى علیه رژیم در نهاوند، آبادان، مشهد، هرمزگان و زابل مورد تعقیب ساواک بود و تظاهرات مردم هرمزگان و زابل در رابطه با قیام سراسرى مردم ایران علیه رژیم پس از سخنرانیهاى ایشان صورت گرفت.
مسئولیتهاى پس از پیروزى انقلاب اسلامى
1 ـ حاکم شرع دادگاههاى انقلاب اسلامى
وى پس از پیروزى انقلاب اسلامى در سمت حاکم شرع دادگاههاى انقلاب اسلامى دزفول، بهبهان، گچساران، بروجرد، رشت و گنبد کاووس خدمت کرد.
2 ـ ریاست دادگاه انقلاب ارتش
با توجه به اهمیت حفظ و اصلاح نیروهاى مسلح پس از پیروزى انقلاب اسلامى و به منظور مقابله با توطئه هاى گروهکهاى ضد انقلاب در این رکن مهم، آیت الله  رى شهرى در اواسط سال 1358 به ریاست دادگاه انقلاب ارتش منصوب شد.
مهمترین مسایلى که در طول تصدى ایشان مورد بررسى قرار گرفت عبارتند از:
الف : رسیدگى به اتهامات متهمین به توطئه در پایگاههاى هوایى تبریز «توطئه حزب منحله خلق مسلمان» اصفهان و بوشهر.
ب : محاکمه متهمین به شرکت در کودتاى نافرجام نوژه 18/4/59.
ج : رسیدگى به ماجراى توطئه قطب زاده.
د : رسیدگى به اتهامات سران نظامى حزب توده.
3 ـ وزیر اطلاعات
وى نخستین وزیر اطلاعات در جمهورى اسلامى ایران است که با کسب آراء اکثریت قاطع نمایندگان مجلس شوراى اسلامى در تاریخ 6/6/1363 به این سمت منصوب گردید. برجسته ترین اقداماتى که در طول تصدى ایشان صورت گرفت عبارتند از:
الف: تأسیس وزارت اطلاعات.
ب : برخورد قاطع با عوامل ترور و بمب گذارى و گروهکهاى ضد انقلاب.
ج : دستگیرى جاسوسان فعال آمریکایى در بخشهاى مختلف.
د : دستگیرى باند منحرف مهدى هاشمى به فرمان امام راحل(ره).
4 ـ دادستان کل کشور
وى در تاریخ 30/5/1368 به دادستانى کل کشور منصوب شد و تا اوایل سال 1370 در این سمت خدمت کرد.
5 ـ دادستان ویژه روحانیت
در تاریخ 13/10/1368 از سوى رهبر معظم انقلاب اسلامى حضرت آیت الله خامنه اى به عنوان دادستان ویژه روحانیت منصوب شد، و در تاریخ 25/12/1375،: از این سمت استعفا داد و با عنایت به اصرار ایشان مقام معظم رهبرى با استعفاى وى موافقت فرمودند .
«جناب عالى را که به حمدالله برخوردار از صلاحیتهاى علمى و عملى و داراى منزلت شایسته اى در نظام جمهورى اسلامى مى باشید به دادستانى این دادگاه منصوب مى کنم».
6 ـ تولیت حضرت عبدالعظیم الحسنى علیه السّلام
وى در تاریخ 20/1/1369 به فرمان رهبر معظم انقلاب اسلامى حضرت آیت الله خامنه اى به تولیت آستان مقدّس عبدالعظیم حسنى علیه السّلام منصوب گردید. انتصاب ایشان آغاز تحول اساسى در عمران و توسعه این مجموعه مهم مذهبى بوده است، مطالعه اولیه و طراحى پروژه هاى مورد نظر با هدف ایجاد امکانات مذهبى، فرهنگى و رفاهى براى زائران، ارتقاء سطح فرهنگى منطقه و ارج نهادن به مقام شامخ اهل بیت پیامبر اسلام صلوات الله علیهم اجمعین از سال 1369 آغاز شد و از سال 1371 به صورت اجرایى درآمد. زیربناى تقریبى طرح یکصدوبیست هزار متر مربع است که در زمینى به مساحب سى وهفت هزار متر مربع در حال احداث است.
