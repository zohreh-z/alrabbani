7 ـ نمایندگى مردم تهران در مجلس خبرگان رهبرى
آیت الله  رى شهرى در انتخابات دومین دوره مجلس خبرگان رهبرى به عنوان داوطلب منفرد شرکت کرد و از سوى مردم استان تهران برگزیده شد، همچنین در سومین دوره انتخابات مجلس خبرگان رهبرى داوطلب منفرد بود که این بار کاندیداتورى ایشان از سوى جامعه محترم مدرسین حوزه علمیه قم و جامعه روحانیت مبارز و سایر گروههاى سیاسى مورد حمایت قرار گرفت و بار رأى قاطع مردم بار دیگر به نمایندگى این مجلس انتخاب گزید.
8 ـ نمایندگى ولى فقیه و سرپرست حجاج
آقاى رى شهرى در تاریخ 6/2/1370 به سمت نماینده مقام معظم رهبرى و سرپرست حجاج منصوب شد، در متن حکم رهبر معظم انقلاب اسلامى آمده است:
«...جناب عالى را که از چهره هاى سرافراز جمهورى اسلامى و داراى مواقف افتخار آمیز در طول سالیان گذشته و همواره کارگزارى امین و شجاع و کارآمد براى انقلاب و پیشواى عظیم راحل آن بوده اید به سمت نماینده خود و سرپرست حجاج محترم ایران منصوب مى کنم».
اقدامات انجام شده:
الف : تحول اساسى در حوزه فعالیتهاى نمایندگى ولى فقیه در حج و تعمیم آن به امور زیارتى با هدف ارائه خدمات اساسى به زائران و گسترش فرهنگ بین المللى حج.
ب : ایجاد ساختار تشکیلاتى و کمیته هاى کارشناسى در زمینه هاى مختلف مربوط به حج و زیارت و فعال نمودن حوزه نمایندگى ولى فقیه در طول سال.
ج : تأسیس مرکز تحقیقات حج با هدف تهیه متون تحقیقى و آموزشى در تبیین ابعاد مختلف حج و زیارت. در این زمینه علاوه بر فصلنامه میقات که تاکنون (فارسى شانزده شماره و عربى پنج شماره) منتشر شده، قریب هفتاد کتاب درزمینه هاى مربوط به حج، چهار کتاب درباره سایر اماکن زیارتى و چهل وپنج کتاب درباره معرفى شیعه و پاسخ به شبهات وهابیان انتشار یافته است.
د : ایجاد واحد هنرى براى تولید فرآورده هاى گوناگون سمعى و بصرى در زمینه موضوعات مربوط به حج و سایر معارف که مورد استقبال علاقمندان و مورد بهره بردارى رسانه ها و به ویژه صدا و سیما قرار گرفته است.
و : نظارت بر امور اجرائى حج که تاکنون منجر به صرفه جویى میلیونها دلار همراه با ارتقاء کیفیت خدمات به زائران گردیده است.
9 ـ ریاست مؤسسه فرهنگى دارالحدیث
آیت الله  رى شهرى بنیانگذار اولین و بزرگترین مجموعه فرهنگى در زمینه حدیث و علوم مربوط به آن مى باشد. این مؤسسه که ریشه در تحقیقات و تألیفات شخصى ایشان دارد و از سالهاى پیش فعالیتهاى خود را آغاز نموده در تاریخ 22/8/1374 مصادف با سالروز ولادت فرخنده حضرت فاطمه زهراعلیها السّلام با پیام مقام مظعم رهبرى و با حضور اندیشمندان و بزرگان حوزه و دانشگاه رسماً افتتاح شد. در بخشى از پیام معظم له آمده است:
«... اکنون تأسیس مجمع دارالحدیث به همّت جناب حجت الاسلام و المسلمین آقاى رى شهرى که در کار حدیث گذشته از شوق و علاقه، تبحر و سابقه و تجربه نیز دارند، این مژده را مى دهد که به حصول این خواسته ها و نیازها در امر حدیث به تدریج نزدیک خواهیم شد. انشاء الله تعالی...»
گستره فعالیتهاى این مؤسسه بخشهاى زیر را در بر مى گیرد:
الف : دانشکده علوم حدیث
با توجه به ضرورت تربیت نیروى انسانى متخصص در امر حدیث، پس از تلاش و پیگیریهاى مستمر، دانشکده علوم حدیث تأسیس و عناوین دروس و واحدهاى سه رشته علم الحدیث، مصطلح الحدیث و رجال الحدیث در تاریخ 2/7/1374 به تصویب شوراى عالى انقلاب فرهنگى رسید.
این دانشکده که با امکاناتى منحصر به فرد و زیربنایى قریب به بیست هزار متر مربع در جوار بارگاه ملکوتى محدّث بزرگ شیعه حضرت عبدالعظیم حسنى علیه السّلام قرار دارد آماده پذیرش دانشجویان و مشتاقان کلام اهل بیت علیهم السّلام مى باشد.
ب : مرکز تحقیقات :
کار پژوهش و تحقیق پیرامون احادیث و علوم مربوط به آن، وظیفه اى است که بر عهده این مرکز مى باشد. این بخش که پرسابقه ترین بخش مؤسسه است، همّت اصلى خود را در تدوین موسوعه اى عظیم از احادیث با نام «موسوعه میزان الحکمة» قرار داده که تاکنون بخش عمده اى از کار آن به پایان رسیده است. همچنین مجموعه هاى مستقلى از این موسوعه منتشر و عرضه شده است که پیش از این به برخى از آنها اشاره شد.
واحدهاى دیگرى چون احیاء متون حدیثى، ترجمه و تدوین، رجال و فقه الحدیث نیز عهده دار پژوهش در زمینه علوم حدیث مى باشند.
ج : مرکز اطلاع رسانى
این مرکز، عهده دار نشر و همگانى نمودن احادیث مى باشد. این هدف بزرگ تاکنون از راههاى مختلفى چون ایجاد کانونهاى حدیث، برگزارى مسابقات، نمایشگاهها و فعالیتهاى فرهنگى، پاسخگویى تلفنى، مکاتبه اى و رایانه اى به پرسشهاى حدیثى و تدوین و انتشار مجموعه هاى متنوع و جذاب حدیثى دنبال شده است.
د : کتابخانه تخصصى حدیث
با توجه به نیاز کار تحقیقاتى به پشتوانه اى قوى از منابع و کتب حدیثى، کتابخانه اى تخصصى در کنار مرکز تحقیقات ایجاد شده است. این کتابخانه که در نوع خود کم نظیر مى باشد با گردآورى بیش از بیست هزار جلد کتاب از داخل و خارج از کشور و ارائه خدمات ویژه به پژوهشگران، به مرجع مناسبى براى شیفتگان معارف اهل بیت علیهم السّلام تبدیل شده است.
هـ : فصلنامه علوم حدیث
این فصلنامه که تنها مجلّه علمى تخصصى حدیث به زبان فارسى مى باشد علاوه بر آن که پایگاهى فکرى و آموزشى در زمینه علوم حدیث است، پلى ارتباطى میان اندیشمندان و علاقمندان به حدیث مى باشد. این نشریه از پاییز 1375 تاکنون هر فصل منتشر شده و دهها مقاله تخصصى و مصاحبه و دیگر اطلاعات لازم را عرضه نموده است.
این مؤسسه علاوه بر فصلنامه علوم حدیث، دوفصلنامه اى نیز با نام «علوم الحدیث» به زبان عربى منتشر مى نماید که مطالب آن با فصلنامه فارسى متفاوت است.
و : انتشارات
واحد انتشارات مؤسسه فرهنگى دارالحدیث کار چاپ و انتشار کتب حدیثى و محصولات تحقیقاتى مؤسسه را سامان داده و از طریق نمایندگى هاى خود در تهران، مشهد، قم و بیروت به عرضه آنها مى پردازد.
این انتشارات تاکنون دهها عنوان کتاب را منتشر و به مشتاقان علم و دانش عرضه نموده است.
10 ـ دبیر کلّى جمعیت دفاع از ارزشهاى انقلاب اسلامى
طى سالهاى اخیر، شمارى از مقامات علمى و سیاسى، ضمن مراجعات مکرّر به آقاى محمّدى رى شهرى، با تشریح شرایط کشور، حضور فعال ایشان را در صحنه سیاسى براى کمک به حل معضلات فرهنگى، اجتماعى، سیاسى و اقتصادى امرى لازم مى خواندند تا اینکه ایشان پس از بررسى و مشورتهاى لازم در پانزدهم خرداد 1374 به همراه جمعى از شخصیتهاى مذهبى و سیاسى، جمعیت دفاع ارزشهاى انقلاب اسلامى را بنیان نهاد.
این جمعیت پس از طى مراحل قانونى رسمیت یافت و هیئت مؤسس آن به اتفاق آراء آیت الله  محمدى رى شهرى را به سمت دبیر کلى جمعیت انتخاب کردند. جمعیت دفاع از ارزشها در تاریخ 24/8/1377 ضمن بیانیه اى تعطیلى موقت فعالیتهاى سیاسى خود را اعلام کرد.
